function initialize() {
    console.log('[TRANSLATION] Initializing page translation system');
    
    // Initialize DOM elements first
    initializeDOMElements();
    
    // Set saved language or default to Romanian
    const savedLang = localStorage.getItem('intex_language') || 'ro';
    console.log('[TRANSLATION] Saved language:', savedLang);
    setLanguage(savedLang);

    // Setup hamburger button for mobile menu
    const hamburgerBtn = document.getElementById('hamburgerBtn');
    // Prefer explicit mobile menu, fall back to any .nav-menu inside <nav>
    let navMenu = document.querySelector('.nav-menu.mobile') || document.querySelector('nav .nav-menu');

    if (hamburgerBtn) {
        console.log('[MENU] hamburger element found:', !!hamburgerBtn);
        hamburgerBtn.addEventListener('click', () => {
            // ensure we have a navMenu reference (in case DOM changed)
            if (!navMenu) navMenu = document.querySelector('.nav-menu.mobile') || document.querySelector('nav .nav-menu');
            console.log('[MENU] hamburger clicked, navMenu present:', !!navMenu, 'navMenu selector:', navMenu && navMenu.className);
            hamburgerBtn.classList.toggle('active');
            // Only toggle nav-menu on small screens (<=1280px)
            if (window.innerWidth <= 1280 && navMenu) {
                navMenu.classList.toggle('active');
                // For debug: log active state after toggle
                console.log('[MENU] navMenu active:', navMenu.classList.contains('active'));
                document.body.style.overflow = navMenu.classList.contains('active') ? 'hidden' : '';
            } else {
                // On large screens, just toggle hamburger active for title display
                console.log('[MENU] Large screen: only toggling hamburger for title display');
            }
        });
        
        // Închide meniul la click pe link
        if (navMenu) {
            navMenu.querySelectorAll('a').forEach(link => {
                link.addEventListener('click', () => {
                    hamburgerBtn.classList.remove('active');
                    if (window.innerWidth <= 1280) {
                        navMenu.classList.remove('active');
                        document.body.style.overflow = '';
                    }
                    document.body.classList.remove('mobile-open');
                });
            });
        }
        // Închide meniul la click pe butonul close din titlul meniului
        const closeMobile = navMenu ? navMenu.querySelector('.close-mobile') : document.querySelector('.close-mobile');
        if (closeMobile) {
            closeMobile.addEventListener('click', () => {
                hamburgerBtn.classList.remove('active');
                if (navMenu && window.innerWidth <= 1280) navMenu.classList.remove('active');
                document.body.style.overflow = '';
                document.body.classList.remove('mobile-open');
            });
        }
    }

    // 2. Dark Mode Toggle: delegate to theme manager if available
    // Theme toggle is handled by the centralized theme manager (`assets/js/theme.js`).
    // Avoid adding another click handler here to prevent double-toggling.

    // 3. Search Overlay
    const searchBtn = document.querySelector('.search-btn');
    const searchOverlay = document.querySelector('.search-overlay');
    const closeSearch = document.querySelector('.search-overlay .close-btn');

    if(searchBtn && searchOverlay && closeSearch) {
        searchBtn.addEventListener('click', () => {
            searchOverlay.style.display = 'flex';
            setTimeout(() => searchOverlay.style.opacity = '1', 10);
        });

        closeSearch.addEventListener('click', () => {
            searchOverlay.style.opacity = '0';
            setTimeout(() => searchOverlay.style.display = 'none', 400);
        });
    }
    
    // Setup login button
    const loginBtn = document.getElementById('loginBtn');
    if (loginBtn) {
        // Check if user is already logged in
        const currentUser = window.authManager.getCurrentUser();
        if (currentUser) {
            updateLoginState(true, currentUser);
        } else {
            loginBtn.addEventListener('click', openLoginModal);
        }
    }
    
    // Setup login forms
    const loginForm = document.getElementById('loginForm');
    if (loginForm) {
        loginForm.addEventListener('submit', handleLogin);
    }
    
    const registerForm = document.getElementById('registerForm');
    if (registerForm) {
        registerForm.addEventListener('submit', handleRegister);
    }
    
    // Setup search form or input
    if (searchInput) {
        const searchForm = document.getElementById('search-form');
        if(searchForm) {
            searchForm.addEventListener('submit', performSearch);
        } else {
            searchInput.addEventListener('keypress', (event) => {
                if (event.key === 'Enter') {
                    performSearch(event);
                }
            });
        }
    }

    // Setup language switchers
    if (langOptions && langOptions.length > 0) {
        langOptions.forEach(span => {
            span.addEventListener('click', () => {
                const lang = span.innerText.toLowerCase();
                console.log('[TRANSLATION] Language switch clicked:', lang);
                setLanguage(lang);
            });
        });
    } else {
        console.warn('[TRANSLATION] No language options found in DOM');
    }
    
    // Dispatch event that page is ready
    try { window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: savedLang } })); } catch (e) { /* ignore */ }
}

